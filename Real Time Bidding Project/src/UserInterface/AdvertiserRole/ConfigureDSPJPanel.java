    private void selectTagButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectTagButtonActionPerformed
        // TODO add your handling code here:
          int selectedRow = tagsJTable.getSelectedRow();
        if(selectedRow>=0)
        {
            TagParameterConfigs tagParameterConfigs = (TagParameterConfigs)tagsJTable.getValueAt(selectedRow, 0);
            tagNameJTextField.setText(tagParameterConfigs.getTagName());
            numberOfVisitsJTextField.setText(String.valueOf(tagParameterConfigs.getPriceOnNumberOfVisits()));
           
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Please select a row to view!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_selectTagButtonActionPerformed

    private void setJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_setJButtonActionPerformed
        // TODO add your handling code here:
        if(tagNameJTextField.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Please select a tag !", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        try{
        int priceOnNumberofVisits=Integer.parseInt(numberOfVisitsJTextField.getText());
        
        organization.getDsp().getTagParameterConfigsList().createOrUpdateTagParameterConfigs(tagNameJTextField.getText(), priceOnNumberofVisits);
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Please enter a valid number!", "Warning", JOptionPane.WARNING_MESSAGE);
            
        }
    }//GEN-LAST:event_setJButtonActionPerformed

