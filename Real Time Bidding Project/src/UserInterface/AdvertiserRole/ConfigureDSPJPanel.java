    private void populateTagTable(){
        DefaultTableModel model = (DefaultTableModel) tagsJTable.getModel();
        
        model.setRowCount(0);
        
        for (TagParameterConfigs tagParameterConfigs : organization.getDsp().getTagParameterConfigsList().getTagParameterConfigsList()){
            Object[] row = new Object[1];
            row[0] = tagParameterConfigs;
         
            model.addRow(row);
        }
    }
    
