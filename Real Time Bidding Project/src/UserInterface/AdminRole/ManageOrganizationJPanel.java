      private void populateCombo(){
       
            organizationJComboBox.removeAllItems();
        for (Type type : Organization.Type.values()){
            
            if(enterprise.getEnterpriseType()==Enterprise.EnterpriseType.InternetAdvertisingPlatform){
            if ((!(type.getValue().equals(Type.Admin.getValue()))&&(!(type.getValue().equals(Type.Consumer.getValue()))))){
                

                    organizationJComboBox.addItem(type);    
            }
            }
            else if (enterprise.getEnterpriseType()==Enterprise.EnterpriseType.Consumer){
                if ((!(type.getValue().equals(Type.Admin.getValue()))&&(type.getValue().equals(Type.Consumer.getValue()))))
                {

                    organizationJComboBox.addItem(type);
                }
            }
        }
    
    }
    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) organizationJTable.getModel();
        
        model.setRowCount(0);
        
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            Object[] row = new Object[2];
            row[0] = organization.getOrganizationID();
            row[1] = organization.getName();            
            model.addRow(row);
        }
    }
