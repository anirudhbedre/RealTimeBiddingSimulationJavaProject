    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed
        boolean consumerOrgExists=false, publisherOrgExists=false,adExOrgExists=false; 
        Type type = (Type) organizationJComboBox.getSelectedItem();
        
        
          for(Organization org: enterprise.getOrganizationDirectory().getOrganizationList()){
                    if(org instanceof PublisherOrganization)
                        publisherOrgExists=true;
                   
                    if(org instanceof AdExchangeOrganization)
                        adExOrgExists=true;
                    
                    if(org instanceof ConsumerOrganization)
                        consumerOrgExists=true;
                    
          }
           
          if(type.getValue().equals(Type.Consumer.getValue()))
              if(consumerOrgExists==true)
              {
                   JOptionPane.showMessageDialog(null,type.getValue()+ " already exists!! ", "Warning", JOptionPane.WARNING_MESSAGE);
                   return;
              }
          if(type.getValue().equals(Type.AdExchange.getValue()))
              if(adExOrgExists==true)
              {
                   JOptionPane.showMessageDialog(null,type.getValue()+ " already exists!! ", "Warning", JOptionPane.WARNING_MESSAGE);
                   return;
              }        
          if(type.getValue().equals(Type.Publisher.getValue()))
              if(publisherOrgExists==true)
              {
                   JOptionPane.showMessageDialog(null,type.getValue()+ " already exists!! ", "Warning", JOptionPane.WARNING_MESSAGE);
                   return;
              }    
        
        
        
        enterprise.getOrganizationDirectory().createOrganization(type);
        populateTable();
    }//GEN-LAST:event_addJButtonActionPerformed

