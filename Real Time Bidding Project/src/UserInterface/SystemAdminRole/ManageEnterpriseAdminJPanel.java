  private void populateAdminTable() {
        DefaultTableModel defaultTableModel = (DefaultTableModel)adminJTable.getModel();
        defaultTableModel.setRowCount(0);
        
        for(Network network:system.getNetworkList())
        {
            for(Enterprise enterprise:network.getEnterpriseDirectory().getEnterPriseList())
            {
                for(UserAccount userAccount:enterprise.getUserAccountDirectory().getUserAccountList())
                {
                    Object row[] = new Object[3];
                    row[0] = enterprise.getName();
                    row[1] = network.getName();
                    row[2] = userAccount.getUsername();
                    
                    defaultTableModel.addRow(row);
                }
            }
        }
    }

    private void populateNetworkCombo() {
        networksJComboBox.removeAllItems();
        
        for(Network network:system.getNetworkList())
        {
            networksJComboBox.addItem(network);
        }
    }
    
