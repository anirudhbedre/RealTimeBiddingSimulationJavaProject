    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        // TODO add your handling code here:
        // Get user name
        String userName = userNameJTextField.getText();
        
          if(system.getUserAccountDirectory().checkIfUserNameUnique(userName)==false){
               JOptionPane.showMessageDialog(this, "The given username already exists! Please enter another one!");
               return;
          }  
          for(Network nw:system.getNetworkList())
          {
            for(Enterprise enterprise:nw.getEnterpriseDirectory().getEnterPriseList())
            {
                
                if(enterprise.getUserAccountDirectory().checkIfUserNameUnique(userName)==false)
                {
                     JOptionPane.showMessageDialog(this, "The given username already exists! Please enter another one!");
                      return;
                }
                for(Organization organization:enterprise.getOrganizationDirectory().getOrganizationList())
                {
                    if(organization.getUserAccountDirectory().checkIfUserNameUnique(userName)==false)
                {
                     JOptionPane.showMessageDialog(this, "The given username already exists! Please enter another one!");
                      return;
                }
                }
            }
           }
        // Get Password
        char[] passwordCharArray = passwordField.getPassword();
        String password = String.valueOf(passwordCharArray);
        String name = nameJTextField.getText();
        
        Network network = (Network)networksJComboBox.getSelectedItem();
        Enterprise enterprise = (Enterprise)enterpriseJComboBox.getSelectedItem();
        if(userName.isEmpty() || password.isEmpty() || name.isEmpty() || network == null || enterprise == null)
        {
            JOptionPane.showMessageDialog(this, "Invalid inputs! Please enter all the values!");
            return;
        }
        Employee employee = enterprise.getEmployeeDirectory().createEmployee(name);
        UserAccount userAccount = enterprise.getUserAccountDirectory().createUserAccount(userName, password, employee, new AdminRole());
        populateAdminTable();
    }//GEN-LAST:event_submitJButtonActionPerformed


